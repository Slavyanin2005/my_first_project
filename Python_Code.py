def get_user_name() -> str:
    """–ó–∞–ø—Ä–∞—à–∏–≤–∞–µ—Ç –∏ –≤–æ–∑–≤—Ä–∞—â–∞–µ—Ç –∏–º—è –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è —Å –≤–∞–ª–∏–¥–∞—Ü–∏–µ–π."""
    while True:
        name = input("–ö–∞–∫ —Ç–µ–±—è –∑–æ–≤—É—Ç? ").strip()
        if name.replace(' ', '').isalpha():
            return name
        print("–ò–º—è –¥–æ–ª–∂–Ω–æ —Å–æ–¥–µ—Ä–∂–∞—Ç—å —Ç–æ–ª—å–∫–æ –±—É–∫–≤—ã. –ü–æ–ø—Ä–æ–±—É–π—Ç–µ –µ—â—ë —Ä–∞–∑.")


def greet_user() -> None:
    """–ü—Ä–∏–≤–µ—Ç—Å—Ç–≤—É–µ—Ç –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è —Å –ø–µ—Ä—Å–æ–Ω–∞–ª–∏–∑–∞—Ü–∏–µ–π."""
    print("üåü –î–æ–±—Ä–æ –ø–æ–∂–∞–ª–æ–≤–∞—Ç—å –≤ –ø—Ä–æ–≥—Ä–∞–º–º—É-–ø—Ä–∏–≤–µ—Ç—Å—Ç–≤–∏–µ! üåü")

    name = get_user_name()
    greeting = (
        f"–ü—Ä–∏–≤–µ—Ç, {name}! –†–∞–¥ —Ç–µ–±—è –≤–∏–¥–µ—Ç—å!"
        if len(name) > 2 else
        f"–ü—Ä–∏–≤–µ—Ç, {name}! –ö–æ—Ä–æ—Ç–∫–æ–µ –∏–º—è, –Ω–æ —Ç–æ–∂–µ –æ—Ç–ª–∏—á–Ω–æ–µ!"
    )
    print(greeting)


def show_menu() -> None:
    """–û—Ç–æ–±—Ä–∞–∂–∞–µ—Ç –º–µ–Ω—é –¥–æ–ø–æ–ª–Ω–∏—Ç–µ–ª—å–Ω—ã—Ö –≤–æ–∑–º–æ–∂–Ω–æ—Å—Ç–µ–π."""
    print("\n–î–æ—Å—Ç—É–ø–Ω—ã–µ –æ–ø—Ü–∏–∏:")
    options = {
        '1': "–ü–æ–∫–∞–∑–∞—Ç—å —Ç–µ–∫—É—â—É—é –¥–∞—Ç—É",
        '2': "–°–≥–µ–Ω–µ—Ä–∏—Ä–æ–≤–∞—Ç—å —Å–ª—É—á–∞–π–Ω–æ–µ —á–∏—Å–ª–æ",
        '0': "–í—ã—Ö–æ–¥"
    }
    for key, value in options.items():
        print(f"[{key}] {value}")


def main() -> None:
    """–û—Å–Ω–æ–≤–Ω–æ–π —Ü–∏–∫–ª –ø—Ä–æ–≥—Ä–∞–º–º—ã."""
    greet_user()

    while True:
        show_menu()
        choice = input("\n–í—ã–±–µ—Ä–∏—Ç–µ –¥–µ–π—Å—Ç–≤–∏–µ: ").strip()

        if choice == '1':
            from datetime import datetime
            print(f"–°–µ–≥–æ–¥–Ω—è: {datetime.now().strftime('%d.%m.%Y')}")
        elif choice == '2':
            from random import randint
            print(f"–°–ª—É—á–∞–π–Ω–æ–µ —á–∏—Å–ª–æ: {randint(1, 100)}")
        elif choice == '0':
            print("–î–æ –Ω–æ–≤—ã—Ö –≤—Å—Ç—Ä–µ—á! üëã")
            break
        else:
            print("–ù–µ–∏–∑–≤–µ—Å—Ç–Ω–∞—è –∫–æ–º–∞–Ω–¥–∞. –ü–æ–ø—Ä–æ–±—É–π—Ç–µ –µ—â—ë —Ä–∞–∑.")


if __name__ == "__main__":
    main()